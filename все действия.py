x = int(input("Введите первое число\t"))
y = int(input("Введите первое число\t"))
print(f'сумма {x} и {y} = {x + y}')
print(f'разность {x} и {y} = {x - y}')
print(f'деление {x} на {y} = {x / y}')
print(f'округление деления до 3 знака {x} на {y} = {round((x/ y),3)}')
print(f'округление деления {x} на {y} = {round((x / y))}')
print(f'умножение{x} на {y} = {x * y}')
print(f'деление без остатка {x} на {y} = {x // y}')
print(f'возведение в степень {x} в {y} = {x ** y}')
print(f'остаток от деления {x} на {y} = {x % y}')
